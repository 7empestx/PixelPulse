void FontRenderer::begin(Display* display) {
    _display = display;
}

void FontRenderer::drawPixel(int x, int y, uint32_t color) {
    _display->drawPixel(x, y, color);
}

int FontRenderer::charWidth(FontSize size) {
    return (size == FONT_4x6) ? 4 : 6;
}

int FontRenderer::charHeight(FontSize size) {
    return (size == FONT_4x6) ? 6 : 8;
}

int FontRenderer::getFontHeight(FontSize size) {
    return charHeight(size);
}

const uint8_t* FontRenderer::getGlyph4x6(char c) {
    int idx = (int)c - 32;
    if (idx < 0 || idx > 63) idx = 0; // fallback to space
    return FONT_4x6_DATA[idx];
}

const uint8_t* FontRenderer::getGlyph6x8(char c) {
    int idx = (int)c - 32;
    if (idx < 0 || idx > 63) idx = 0;
    return FONT_6x8_DATA[idx];
}

int FontRenderer::drawChar(int x, int y, char c, uint32_t color, FontSize size) {
    c = toupper(c);
    int w = charWidth(size);
    int h = charHeight(size);

    if (size == FONT_4x6) {
        const uint8_t* glyph = getGlyph4x6(c);
        for (int row = 0; row < h; row++) {
            uint8_t line = glyph[row];
            for (int col = 0; col < w; col++) {
                if (line & (0x80 >> col)) {
                    drawPixel(x + col, y + row, color);
                }
            }
        }
    } else {
        const uint8_t* glyph = getGlyph6x8(c);
        for (int row = 0; row < h; row++) {
            uint8_t line = glyph[row];
            for (int col = 0; col < w; col++) {
                if (line & (0x80 >> col)) {
                    drawPixel(x + col, y + row, color);
                }
            }
        }
    }
    return w + 1; // 1px spacing
}

void FontRenderer::drawString(int x, int y, const char* str, uint32_t color, FontSize size) {
    int cx = x;
    while (*str) {
        cx += drawChar(cx, y, *str, color, size);
        str++;
    }
}

void FontRenderer::drawStringCentered(int y, const char* str, uint32_t color, FontSize size) {
    int w = getStringWidth(str, size);
    int x = (PANEL_WIDTH - w) / 2;
    if (x < 0) x = 0;
    drawString(x, y, str, color, size);
}

int FontRenderer::getStringWidth(const char* str, FontSize size) {
    int len = strlen(str);
    if (len == 0) return 0;
    int cw = charWidth(size);
    return len * (cw + 1) - 1; // no trailing space
}

void FontRenderer::drawCursor(int x, int y, uint32_t color, FontSize size) {
    if ((millis() / 500) % 2 == 0) {
        int h = charHeight(size);
        for (int row = 0; row < h; row++) {
            drawPixel(x, y + row, color);
            drawPixel(x + 1, y + row, color);
        }
    }
}
